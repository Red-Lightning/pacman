    public Level() {
        mazeTexture = Assets.instance.levelAssets.maze;
        pacman = new Pacman();
        moveState = MoveState.STATIONARY;
    }

    private boolean canMoveRight() {
        int[] loc = pacman.getLoc();
        if(tiles[loc[0]][loc[1]+1] != 0 && loc[1]+1 < Constants.MAZE_LENGTH) return true;
        return false;
    }

    public void update() {
        switch (moveState) {
            case UP:
                break;
            case DOWN:
                break;
            case LEFT:
                break;
            case RIGHT:
                if(canMoveRight()) { pacman.moveRight(); }
                break;
            case STATIONARY:
                break;
        }
    }

    @Override
    public boolean keyDown(int keycode) {
        if(keycode == Input.Keys.RIGHT) {
            if( canMoveRight() ) {
                moveState = MoveState.RIGHT;
                Gdx.app.log("Key Press", "Functioning as intended!");
            }
        }
        return true;
    }

    public void render(SpriteBatch batch) {
        batch.draw(
                mazeTexture.getTexture(),
                15, 15,
                0, 0,
                mazeTexture.getRegionWidth(),
                mazeTexture.getRegionHeight(),
                1,
                1,
                0,
                mazeTexture.getRegionX(),
                mazeTexture.getRegionY(),
                mazeTexture.getRegionWidth(),
                mazeTexture.getRegionHeight(),
                false,
                false);

        pacman.update();
        pacman.render(batch);
    }


    // Create an enum to hold pacmans move state
    enum MoveState {
        UP, DOWN, LEFT, RIGHT, STATIONARY;
    }
